import axios from 'axios';
import React from 'react';
import { DataGrid } from '@mui/x-data-grid';

/**
 * A DataTable is responsible for displaying data in an organizable
 * fashion for a user.
 * 
 * Constraints: Rows must be JSON-serializable key-value pairs that
 * can be mapped to individual rows. Data headers will be set to the
 * corresponding PascalCase string parsed from the data's keyset.
 * @property data represents the data to pass in
 * @property headerAutoGenerate whether the header row should auto-compute
 * @returns {JSX.Element}
 */
const DataTable = (props) => {

    const exampleColumns = [
        { field: 'id', headerName: 'ID', width: 90 },
        {
            field: 'firstName',
            headerName: 'First name',
            width: 150,
            editable: true,
        },
        {
            field: 'lastName',
            headerName: 'Last name',
            width: 150,
            editable: true,
        },
        {
            field: 'age',
            headerName: 'Age',
            type: 'number',
            width: 110,
            editable: true,
        },
        {
            field: 'fullName',
            headerName: 'Full name',
            description: 'This column has a value getter and is not sortable.',
            sortable: false,
            width: 160,
            valueGetter: (params) =>
                `${params.row.firstName || ''} ${params.row.lastName || ''}`,
        },
    ];

    // Example rows.
    const exampleRows = [
        { id: 1, lastName: 'Snow', firstName: 'Jon', age: 35 },
        { id: 2, lastName: 'Lannister', firstName: 'Cersei', age: 42 },
        { id: 3, lastName: 'Lannister', firstName: 'Jaime', age: 45 },
        { id: 4, lastName: 'Stark', firstName: 'Arya', age: 16 },
        { id: 5, lastName: 'Targaryen', firstName: 'Daenerys', age: null },
        { id: 6, lastName: 'Melisandre', firstName: null, age: 150 },
        { id: 7, lastName: 'Clifford', firstName: 'Ferrara', age: 44 },
        { id: 8, lastName: 'Frances', firstName: 'Rossini', age: 36 },
        { id: 9, lastName: 'Roxie', firstName: 'Harvey', age: 65 },
    ];

    /**
     * Represents the column set of the table. If props.headerAutoGenerate is specified,
     * then the table will provide a generic header that is automatically generated by
     * the keyset of the data array.
     * @type {{headerName: *, field: *, editable: boolean, width: number, type}[]|*}
     */
    const col = Object.keys(props.data[0]).map(entry => {
        return {
            field: entry,
            headerName: entry.toUpperCase(),
            type: typeof entry,
            editable: true,
            width: 150
        }
    })

    return (
      <div style={{ height: 600, width: '100%' }}>
          <DataGrid
              rows={props.data}
              columns={props.headerAutoGenerate ? col : props.columns}
              pageSize={15}
              rowsPerPageOptions={[5]}
              checkboxSelection
              disableSelectionOnClick
          />
      </div>
  );
};

/**
 * A DataTable is responsible for displaying data in an organizable
 * fashion for a user.
 * @returns {JSX.Element}
 */
export default DataTable;
